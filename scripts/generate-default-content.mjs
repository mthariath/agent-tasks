import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");

const files = {
  GENERATED_DEFAULT_TASKS_README: "templates/default/.agent-tasks/README.md",
  GENERATED_DEFAULT_WORKFLOW_GUIDE: "templates/default/.agent-tasks/WORKFLOW.md",
  GENERATED_DEFAULT_AGENT_GUIDANCE_BLOCK: "templates/default/AGENTS.block.md",
  GENERATED_DEFAULT_GITIGNORE_BLOCK: "templates/default/gitignore.block.txt",
  GENERATED_DEFAULT_TICKET_TEMPLATE: "templates/default/.agent-tasks/templates/ticket.md",
  GENERATED_DEFAULT_EPIC_TEMPLATE: "templates/default/.agent-tasks/templates/epic.md",
  GENERATED_DEFAULT_EXTENSIONS_README: "templates/default/.agent-tasks/extensions/README.md",
  GENERATED_DEFAULT_EXTENSIONS_API: "templates/default/.agent-tasks/extensions/api.ts",
  GENERATED_DEFAULT_EXTENSION_REVIEW_QUEUE: "templates/default/.agent-tasks/extensions/examples/review-queue.ts.example",
  GENERATED_DEFAULT_EXTENSION_AUTO_TEST_ON_FINISH: "templates/default/.agent-tasks/extensions/examples/auto-test-on-finish.ts.example",
  GENERATED_DEFAULT_EXTENSION_MERGE_HANDOFF: "templates/default/.agent-tasks/extensions/examples/merge-handoff.ts.example",
  GENERATED_DEFAULT_EXTENSION_STALE_WORKER: "templates/default/.agent-tasks/extensions/examples/stale-worker.ts.example",
  GENERATED_DEFAULT_SKILL_CREATING_TICKETS: "skills/default-workflow/creating-tickets/SKILL.md",
  GENERATED_DEFAULT_SKILL_REFINING_TICKETS: "skills/default-workflow/refining-tickets/SKILL.md",
  GENERATED_DEFAULT_SKILL_WORKING_TICKETS: "skills/default-workflow/working-tickets/SKILL.md",
  GENERATED_DEFAULT_SKILL_MANAGING_STATUS: "skills/default-workflow/managing-status/SKILL.md"
};

const entries = await Promise.all(
  Object.entries(files).map(async ([name, relativePath]) => {
    const fullPath = path.join(repoRoot, relativePath);
    const contents = await readFile(fullPath, "utf8");
    return {
      name,
      relativePath,
      contents: contents.replace(/\s*$/, "")
    };
  })
);

const output = [
  "// This file is generated by scripts/generate-default-content.mjs.",
  "// Do not edit it directly; edit the source files in skills/default-workflow or templates/default instead.",
  ""
];

for (const entry of entries) {
  output.push(`// Source: ${entry.relativePath}`);
  output.push(`export const ${entry.name} = ${JSON.stringify(entry.contents)};`);
  output.push("");
}

await writeFile(
  path.join(repoRoot, "packages/core/src/generated-default-content.ts"),
  `${output.join("\n")}\n`,
  "utf8"
);
